# ── Build data files ──────────────────────────────────────────────────────────
def build_caustic_dat(fit):
    """z_um  w_um  (measured points)"""
    lines = ["# z_um\tw_um"]
    for z, w in zip(fit["z_data"], fit["w_data"]):
        lines.append(f"{z:.2f}\t{w:.2f}")
    return "\n".join(lines)


def build_caustic_fit_dat(fit):
    """z_um  w_um  (fit curve, dense)"""
    lines = ["# z_um\tw_fit_um"]
    for z, w in zip(fit["z_fit"], fit["w_fit"]):
        lines.append(f"{z:.2f}\t{w:.2f}")
    return "\n".join(lines)


