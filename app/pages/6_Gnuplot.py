# ── Run gnuplot ───────────────────────────────────────────────────────────────
def run_gnuplot(script, data_files=None):
    """
    Execute a gnuplot script, return (success, png_bytes, stderr).
    data_files: dict of {filename: content_string} to write alongside the script.
    """
    with tempfile.TemporaryDirectory() as tmpdir:
        # Write data files
        if data_files:
            for fname, content in data_files.items():
                with open(os.path.join(tmpdir, fname), "w") as f:
                    f.write(content)

        # Write script
        gp_path = os.path.join(tmpdir, "plot.gp")
        with open(gp_path, "w") as f:
            f.write(script)

        # Run gnuplot
        result = subprocess.run(
            [GNUPLOT_BIN, gp_path],
            cwd=tmpdir,
            capture_output=True,
            timeout=15,
        )

        # Find the output PNG
        png_bytes = None
        for fname in os.listdir(tmpdir):
            if fname.endswith(".png"):
                with open(os.path.join(tmpdir, fname), "rb") as f:
                    png_bytes = f.read()
                break

        return (result.returncode == 0, png_bytes, result.stderr.decode("utf-8", errors="replace"))


